var grade=window.prompt("Enter your Grade");

switch(grade){
    case 'A':document.write("EXCELLENT");
    break;

    case 'B':document.write("GOOD JOB");
    break;

    case 'C':document.write("AVERAGE");
    break;

    case 'D':document.write("WORK HARD");
    break;
    
    case 'E':document.write("YOUR TIME HAS COME");
    break;

    case 'F':document.write("FAIL");
    break;

    default:document.write("Unknown Grade");
    }

